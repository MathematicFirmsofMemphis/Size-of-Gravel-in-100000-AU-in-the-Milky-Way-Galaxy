			- **Gravel-Sized Rock Concentration**: 4.8%

			### Definitions:
			- **Astronomical Unit (AU)**: The average distance from the Earth to the Sun, approximately 149.6 million kilometers.

			### Conversion to Standard Units:
			To understand the scale in more familiar units:
			- 1 AU ≈ 149.6 million kilometers
			- 1 AU³ (cubic AU) ≈ (149.6 million kilometers)³

			For simplicity in this calculation, we will keep the volume in astronomical units.

			### Calculating the Volume of Gravel-Sized Rocks:
			The volume of gravel-sized rocks is calculated by multiplying the total volume by the percentage concentration of these rocks.

			//[
				//text{Volume of gravel-sized rocks} = //text{Total Volume} //times //left( //frac{//text{Percentage of gravel-sized rocks}}{100} //right)
				//]

			Substituting the given values:

			//[
				//text{Volume of gravel-sized rocks} = 100,000 //, //text{AU} //times //left( //frac{4.8}{100} //right)
